console.log("Comparison Operators");

let intValue = 70;
let stringValue = "70";

/* 
* Important double == just performs value comparison and not a type comparison
* Important triple === performs the value comparison and types of each operands (number, strings)

*/
console.log("intValue == stringValue is ", intValue == stringValue);
console.log("intValue === stringValue is ", intValue === stringValue);
// * Reverse of double equal and triple equal
console.log("intValue != stringValue is ", intValue != stringValue);
console.log("intValue !== stringValue is ", intValue !== stringValue);

console.log("intValue > stringValue is ", intValue > stringValue);
console.log("intValue < stringValue is ", intValue < stringValue);
//* Creates and implicit type of conversion
console.log("intValue >= stringValue is ", intValue >= stringValue);
console.log("intValue <= stringValue is ", intValue <= stringValue);

