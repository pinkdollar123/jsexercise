"use strict";

console.log("Arithmetic Operators");

let x = 20;
let y = 6;

console.log("Value of x : ", x);
console.log("Value of y : ", y);

console.log("The Sum of x and y is = ", x + y);
var concatenatedString = "We" + " " + "are" + " " + "learning" + " " + "JS";
console.log("String Concatenation", concatenatedString);

console.log("Subtraction of y from x = ", y - x);
console.log("Product of x from y = ", x * y);
console.log("Division of x from y = ", x / y);

console.log("Modulus of x from y = ", x % y); // Remainder of 20 / 6 = 2

console.log("Value of x", x);
console.log("Printing out x++ :", x++); // post increment method

console.log("Value of x", x);
console.log("Printing out ++x :", ++x); // increment the value before it's printed out

console.log ("Value of x", x);
console.log ("Value of x--", x--);

console.log ("Value of x", x);
console.log ("Value of --x", --x);

console.log("Assignment Operators");

y=x;
console.log("value of x is assigned to y, so y = ", y);

x += 1;
console.log("Value of x after performing x += 1 is ", x);

y -= 1;
console.log("Value of y after performing y -= 1 is", y);

